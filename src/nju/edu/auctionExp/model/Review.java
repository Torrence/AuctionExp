package nju.edu.auctionExp.model;

/**
 * Review entity. @author MyEclipse Persistence Tools
 */

public class Review implements java.io.Serializable {

	// Fields

	/**
	 * 
	 */
	private static final long serialVersionUID = -1481294284534581700L;
	private String id;
	private int rating;
	private String author;
	private String summary;
	private String content;

	// Constructors

	/** default constructor */
	public Review() {
	}
	
	public Review(String id){
		this.id = id;
		this.rating = 0;
		this.author = "";
		this.content = "";
		this.summary = "";
	}

	/** full constructor */
	public Review(String id, int rating, String author, String summary,
			String content) {
		this.id = id;
		this.rating = rating;
		this.author = author;
		this.summary = summary;
		this.content = content;
	}

	// Property accessors

	public String getId() {
		return this.id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getRating() {
		return this.rating;
	}

	public void setRating(int rating) {
		this.rating = rating;
	}

	public String getAuthor() {
		return this.author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getSummary() {
		return this.summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public String getContent() {
		return this.content;
	}

	public void setContent(String content) {
		this.content = content;
	}

}